//randFontFrom 随机的选择一种字体
func randFontFrom(fonts []*truetype.Font) *truetype.Font {
	fontCount := len(fonts)

	if fontCount == 0 {
		fonts = fontsAll
		fontCount = len(fontsAll)
	}
	index := rand.Intn(fontCount)
	return fonts[index]
}

